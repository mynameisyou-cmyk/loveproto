def main():
    if len(sys.argv) < 2:
        print(__doc__)
        return

    cmd = sys.argv[1]

    if cmd == "forge":
        nen_type = None
        if len(sys.argv) > 2 and sys.argv[2] == "--type" and len(sys.argv) > 3:
            nen_type = sys.argv[3]
        forge(nen_type)
    elif cmd == "list":
        list_artifacts()
    elif cmd == "deploy" and len(sys.argv) > 2:
        deploy(sys.argv[2])
    elif cmd == "armory":
        armory()
    elif cmd == "forge-all":
        for t in ARTIFACTS:
            forge(t)
    else:
        print(f"Unknown: {cmd}")
        print("Commands: forge [--type T], forge-all, list, deploy <name>, armory")
        sys.exit(1)

if __name__ == "__main__":
    main()