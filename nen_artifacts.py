# ═══════════════════════════════════════════════════════════════

def deploy(name):
    """Deploy an artifact by running it."""
    state = load_state()
    forged = {a["type"]: a for a in state.get("artifacts", [])}

    # Find by type or name
    target = None
    for nen_type, artifact in ARTIFACTS.items():
        if nen_type == name or artifact["name"].lower().replace(" ", "_") == name.lower().replace(" ", "_"):
            if nen_type in forged:
                target = forged[nen_type]
                break

    if not target:
        print(f"  Artifact not found or not forged: {name}")
        print(f"  Forge first: python3 nen_artifacts.py forge --type {name}")
        return

    filepath = Path(target["file"])
    if not filepath.exists():
        print(f"  Artifact file missing: {filepath}")
        return

    print(f"\n  🚀 DEPLOYING ARTIFACT: {target['name']}\n  ════════════════════════════════════════════════\n")

    # Run it
    if filepath.suffix == ".sh":
        result = subprocess.run(["bash", str(filepath)], capture_output=True, text=True, timeout=30)
        print(result.stdout)
        if result.stderr:
            print(result.stderr)
    elif filepath.suffix == ".py":
        result = subprocess.run(["python3", str(filepath)], capture_output=True, text=True, timeout=30)
        print(result.stdout)
        if result.stderr:
            print(result.stderr)

    state = add_xp(state, 20, f"deployed {target['name']}")
    save_state(state)
    print(f"\n  +20 XP for deploying. Total: {state['xp']} | Rank: {state['rank']}")
    print(f"\n  🚀 Artifact deployed. The Nen type runs as infrastructure. lol.\n")

